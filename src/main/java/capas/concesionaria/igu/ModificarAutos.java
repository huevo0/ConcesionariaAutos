    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // Obtener datos
        String marca = txtMarca.getText();
        String modelo = txtModelo.getText();
        String motor = txtMotor.getText();
        String color = txtColor.getText();
        String patente = txtPatente.getText();
        int numPuertas = Integer.parseInt(txtNumPuertas.getText());
        
        control.modificarAuto(auto,marca, modelo, motor, color, patente, numPuertas);
        mostrarMensaje("Auto Guardado Correctamente", "Info", "Alta Creada");
        
        VerAutos pantalla = new VerAutos();
        pantalla.setVisible(true);
        pantalla.setLocationRelativeTo(null);
        this.dispose();
        
    }//GEN-LAST:event_btnGuardarActionPerformed

     public void mostrarMensaje ( String mensaje, String tipo, String titulo){
         JOptionPane optionPane = new JOptionPane(mensaje);
         if(tipo.equalsIgnoreCase("Info")){
             optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
         }
         else if(tipo.equalsIgnoreCase("Error")) {
             optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
          
         }
         JDialog dialog = optionPane.createDialog(titulo);
         dialog.setVisible(true);
         dialog.setAlwaysOnTop(true);
         dialog.setLocationRelativeTo(null);
    }
